ll getAdd(ll x) {
    string str = to_string(x);
    int maxi = 0;
    int mini = 10;
    for(char ch: str) {
        maxi = max(maxi, ch-'0');
        mini = min(mini, ch-'0');
    }

    return maxi*(ll)mini;
}

void solve() {
    ll x, k;
    cin >> x >> k;
    k--;
    while(k--) {
        ll y = getAdd(x);
        if(y == 0) break;
        x += y;
    }

    cout << x << endl;
}

